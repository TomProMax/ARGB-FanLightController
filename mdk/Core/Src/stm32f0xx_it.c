//串口IDLE回调函数
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
	if(huart->Instance == USART1)
	{
		//接收长度
		buf_ptr = Size;
		//寻找冒号分隔符位置
		char *str_ptr = strstr((char *)uart_rx_buf,":");
		//保存last val
		CPU_Temp[0] = CPU_Temp[1];
		//更新cur val
		CPU_Temp[1] = atoi(str_ptr + 1);
		//打印接收到的数据
		printf("Size:%d,CPUTemp:%d\r\n",Size,CPU_Temp[1]);
		//清零数组
		memset(uart_rx_buf, '\0', Size);
		//重启服务
		HAL_UARTEx_ReceiveToIdle_DMA(&huart1,uart_rx_buf,50);
		//信息更新
		info_update_flag = 1;
	}
}
